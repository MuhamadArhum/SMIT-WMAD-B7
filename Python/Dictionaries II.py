# This is A Dictionary which have multiple type of Values.

dict = {
    "name": "Arhum",         # String value
    "age": "20",               # Integer value
    "height": 5.8,           # Float value
    "is_student": True,      # Boolean value
    "scores": [95, 88, 92]   # List as a value
}

# Accessing values in the dictionary

print( dict["name"])        # Output
print( dict["age"])         # Output
print( dict["height"])      # Output
print( dict["is_student"])  # Output
print( dict["scores"])      # Output
