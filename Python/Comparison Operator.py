# In this ,we will learn about Comparison Operator.
# For checking True or False Value.
# Note
# Python has 6 Comparison Operators.

print( 3 > 2 ) # Greater than Comparison  Operator.
print( 2 < 3 ) # Less Than Comparison Operator.

print( 3 >= 2 ) # Greater than equal to Comparison  Operator.
print( 2 <= 3 ) # Less Than equal to Comparison Operator.

print( 3 == 3 ) # Equal to Comparison  Operator.
print( 3 != 4 ) #  Not Equal to Comparison  Operator.

