# In this ,we will learn about Logical Operators.
# Note
# Python has 3 Logical Operators.

print( 2 > 3 or 2 > 1 ) # OR Operator.
# if any 1 input is true, The output is True.

print( 3 > 2 and 2 > 5 ) # AND operator.
# if Both inputs are True ,The ouput is True.

print ( not 5 > 2 ) # Not Operator.
# if the input is True, The output is False
