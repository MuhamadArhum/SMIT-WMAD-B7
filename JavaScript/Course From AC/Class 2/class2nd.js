// Operators in JS
let a = 5;
let b = 3;

console.log("a+b=", a+b);  // Addition Operator
console.log("a-b=", a-b);  // Subtraction or Minus Operator
console.log("a/b=", a/b);  // Division Operator
console.log("a*b=", a*b);  // Multiplication Operator
console.log("a%b=", a%b);  // Modulus Operator

// Unary Operators (Increamet & Decrement) 
// Using Above Values....
a++;  // Increment Operator
console.log("a=", a++);

b--;  // Decrement Operator
console.log("b=", b);


// Assignment Operators
// a = 6
// b = 2
console.log("Addition Assignment a=",a+=b);          // (a=6 & b=4)
console.log("Subtraction Assignment a=", a-=b);      // (a=9 & b=2)
console.log("Multiplication Assignment a=", a*=b);   // (a=7 & b=2)
console.log("Division Assignment a=", a/=b);         // (a=14 & b=2)
console.log("Modulus Assignment a=", a%=b);          // (a=7 & b=2)


// Comparison Operators
// a = 1
// b = 2
console.log("Equal Operator", a==b);                 // Output in Boolean Expression
console.log("Strict Equal Operator", a===b);         // Output in Boolean Expression
console.log("Not Equal Operator", a!=b);             // Output in Boolean Expression
console.log("Strict Not Equal Operator", a!==b);     // Output in Boolean Expression
console.log("Greater than Operator", a>b);           // Output in Boolean Expression
console.log("Less than Operator", a<b);              // Output in Boolean Expression
console.log("Greater than or Equal to Operator", a>=b);// Output in Boolean Expression
console.log("Less than or Equal to Operator", a<=b); // Output in Boolean Expression


